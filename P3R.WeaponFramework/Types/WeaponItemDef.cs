internal static partial class WeaponExtensions
{
    public static string GetNameString(this WeaponItemDef weaponItem)
    {
        var assembly = Assembly.GetExecutingAssembly();
        var resourceName = "P3R.WeaponFramework.Resources.EN.WeaponNames.json";
        var dict = P3RWF_Dictionary<WeaponItemDef, string>.LoadJson(assembly, resourceName);
        return dict[weaponItem];
    }
    public static FString GetFString(this WeaponItemDef weaponItem)
    {
        return weaponItem.GetNameString().MakeFString();
    }
